const fs = require('fs');
const path = require('path');

function formatBuildId(date) {
  const pad = (n) => String(n).padStart(2, '0');
  const yyyy = date.getUTCFullYear();
  const mm = pad(date.getUTCMonth() + 1);
  const dd = pad(date.getUTCDate());
  const hh = pad(date.getUTCHours());
  const min = pad(date.getUTCMinutes());
  const ss = pad(date.getUTCSeconds());
  return `${yyyy}${mm}${dd}-${hh}${min}${ss}Z`;
}

const projectRoot = path.resolve(__dirname, '..');
const pkgPath = path.join(projectRoot, 'package.json');
const outPath = path.join(projectRoot, 'js', 'build-info.js');
const configXmlPath = path.join(projectRoot, 'config.xml');

const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
const version = pkg.version || '0.0.0';

// Sync version into config.xml
const configXml = fs.readFileSync(configXmlPath, 'utf8');
const updatedConfigXml = configXml.replace(
  /(<widget[^>]+version=")[^"]*(")/,
  `$1${version}$2`
);
if (updatedConfigXml !== configXml) {
  fs.writeFileSync(configXmlPath, updatedConfigXml, 'utf8');
  console.log(`[build-info] synced config.xml version => ${version}`);
}

const now = new Date();
const builtAt = now.toISOString();
const buildId = formatBuildId(now);

const apiBase = process.env.API_BASE || '';
const wsUrl = process.env.WS_URL || '';
const playerConfigLines = [];
if (apiBase) playerConfigLines.push(`    API_BASE: ${JSON.stringify(apiBase)}`);
if (wsUrl) playerConfigLines.push(`    WS_URL: ${JSON.stringify(wsUrl)}`);
const playerConfigBlock = playerConfigLines.length
  ? `\n  // Dev config overrides (injected at build time)\n  window.__PLAYER_CONFIG__ = {\n${playerConfigLines.join(',\n')}\n  };\n`
  : '';

const content = `// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by scripts/generate-build-info.cjs
(function () {
  var info = {
    version: ${JSON.stringify(version)},
    buildId: ${JSON.stringify(buildId)},
    builtAt: ${JSON.stringify(builtAt)}
  };
  window.PLAYER_BUILD_INFO = info;
  window.PLAYER_DEPLOY_VERSION = info.version + ' ' + info.buildId;
${playerConfigBlock}})();
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, content, 'utf8');

console.log(`[build-info] wrote ${path.relative(projectRoot, outPath)} => ${version} ${buildId}`);
